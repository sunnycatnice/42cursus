#include "libft.h"

void	ft_if_free_mtx(char ***m)
{
	int	i;

	i = 0;
	if (!(*m))
		return ;
	while ((*m)[i])
		ft_if_free((void **)&(*m)[i++]);
	ft_if_free((void **)&(*m));
}
