#include "../../includes/philosophers.h"

int	check_bychar(char const *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if (c[i] >= '0' && c[i] <= '9')
			i++;
		else
			return (1);
	}
	return (0);
}
